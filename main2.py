def add(x,y):
    return x+y
def subract(z,a):
    return z-a
def multiply(b,c):
    return b*c
def divide(d,g):
    return d/g

para1=int(input("Enter the first value : "))
para2=int(input("Enter the second value : "))

print(add(para1,para2))
print(subract(para1,para2))
print(multiply(para1,para2))
print(divide(para1,para2))